package diet;

public class RawMaterial implements NutritionalElement {

	public RawMaterial(String name, double calories, double proteins,
			double carbs, double fat) {
		super();
		this.name = name;
		this.calories = calories;
		this.proteins = proteins;
		this.carbs = carbs;
		this.fat = fat;
	}

	String name = new String();
	double calories;
	double proteins;
	double carbs;
	double fat;
	boolean per100g;
	
	@Override
	public String getName() {
		return this.name;
	}

	@Override
	public double getCalories() {
		return calories;
	}

	@Override
	public double getProteins() {
		return proteins;
	}

	@Override
	public double getCarbs() {
		return carbs;
	}

	@Override
	public double getFat() {
		return fat;
	}

	@Override
	public boolean per100g() {
		return true;
	}

}
